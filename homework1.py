# Задача 4:
# Петя, Катя и Сережа делают из бумаги журавликов. Вместе они сделали S журавликов.
# Сколько журавликов сделал каждый ребенок, если известно, что Петя и Сережа (P) сделали одинаковое количество журавликов,
# а Катя (K) сделала в два раза больше журавликов, чем Петя и Сережа вместе?
# *Пример:*
# 6 -> 1  4  1
# 24 -> 4  16  4
# 60 -> 10  40  10

# Решение:
# Из условия "Катя сделала в два раза больше журавликов, чем Петя и Сережа вместе" следует:
# S = K + (2 * Р)
# K = (P + P) * 2
# S = (P + P) * 2 + (2 * Р) = 2P + 2P + 2P = 6P
# Общее количество журавликов будет кратно 6.

print(" *** Задача №4 *** ")

s = int(input("Введите общее количество журавликов: "))

p = s // 6
k = 2 * (p + p)

print("Петя сделал", p, "журавликов")
print("Катя сделала", k, "журавликов")
print("Сережа сделал", p, "журавликов")

print("========= Конец зачачи №4 ========== \n")



