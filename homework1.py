# Задача 8:
# Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек,
# если разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).
# *Пример:*
# 3 2 4 -> yes
# 3 2 1 -> no


# Решение:
print(" *** Задача №8 *** ")

# Запрашиваем у пользователя размеры шоколадки и количество долек, которые необходимо отломить
print("Введите ширину и высоту шоколадки, и количество долек, которое нужно отломить. ")
n, m, k = map(int, input("Введите все числа через пробел:").split())

# Проверяем, можно ли отломить k долек.
# Количество отламываемых долек должно быть не больше общего количества долек n * m.
# Количество отламываемых долек должно быть кратно n или m.
if k <= n*m and (k % n == 0 or k % m == 0):
    print(f"Да! Можно отломить {k} долек одним разломом!")
else:
    print(f"К сожалению, одним разломом отломить {k} долек не возможно!")

print("========= Конец зачачи №8 ==========")
